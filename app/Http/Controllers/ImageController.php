    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Image  $image
     * @return \Illuminate\Http\Response
     */
    public function destroy(Image $image)
    {
        $images = Image::find($image);
        Storage::disk("public")->delete($images->img);
        $images->delete();
        return back();
    }

